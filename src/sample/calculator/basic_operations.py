def add(a, b):
    """Складывает два числа.

    Args:
        a (float): Первое число.
        b (float): Второе число.

    Returns:
        float: Результат сложения.
    """
    return a + b

def subtract(a, b):
    """Вычитает одно число из другого.

    Args:
        a (float): Уменьшаемое.
        b (float): Вычитаемое.

    Returns:
        float: Результат вычитания
    """
    return a - b
